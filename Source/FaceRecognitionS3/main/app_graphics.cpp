void draw_rect(uint16_t *buf, int w, int h, int x1, int y1, int x2, int y2, uint16_t color) {
    // Clip coordinates
    x1 = (x1 < 0) ? 0 : (x1 >= w) ? w - 1 : x1;
    x2 = (x2 < 0) ? 0 : (x2 >= w) ? w - 1 : x2;
    y1 = (y1 < 0) ? 0 : (y1 >= h) ? h - 1 : y1;
    y2 = (y2 < 0) ? 0 : (y2 >= h) ? h - 1 : y2;

    // Horizontal lines (optimized with contiguous writes)
    uint16_t *line1 = &buf[y1 * w + x1];
    uint16_t *line2 = &buf[y2 * w + x1];
    int width = x2 - x1 + 1;
    for (int i = 0; i < width; i++) {
        line1[i] = color;
        line2[i] = color;
    }

    // Vertical lines
    uint16_t *p1 = &buf[y1 * w + x1];
    uint16_t *p2 = &buf[y1 * w + x2];
    for (int i = 0; i <= (y2 - y1); i++) {
        *p1 = color;
        *p2 = color;
        p1 += w;
        p2 += w;
    }
}

void draw_char(uint16_t *buf, int w, int h, int x, int y, char c, uint16_t color) {
    if (c < 32 || c > 127 || x < 0 || x + 8 >= w || y < 0 || y + 8 >= h) return;
    int idx = c - 32;
    uint16_t *p = &buf[y * w + x];
    for (int i = 0; i < 8; i++) {
        uint8_t line = font8x8_basic[idx][i];
        for (int j = 0; j < 8; j++) {
            if (line & (1 << (7 - j))) { 
                p[j] = color;
            }
        }
        p += w;
    }
}

void draw_string(uint16_t *buf, int w, int h, int x, int y, const char *str, uint16_t color) {
    while (*str) {
        draw_char(buf, w, h, x, y, *str++, color);
        x += 8;
        if (x + 8 >= w) break;
    }
}
